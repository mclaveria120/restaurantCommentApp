package finalproject.dto;

public class RestaurantInfo {

	private String name;
	private String numberOfRewviews;
	private Integer id;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNumberOfRewviews() {
		return numberOfRewviews;
	}
	public void setNumberOfRewviews(String numberOfRewviews) {
		this.numberOfRewviews = numberOfRewviews;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	
}
